function updateTexts() {
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
      const keys = el.getAttribute('data-i18n').split(';'); // Если несколько ключей, разделенных ;
      let text = '';
      keys.forEach(key => {
        text += i18next.t(key.trim()) + ' '; // Получаем перевод для каждого ключа и добавляем к тексту
      });
      el.innerHTML = text.trim(); // Устанавливаем HTML-содержимое элемента
    });
  }
  
