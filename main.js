function updateTexts() {
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
      const keys = el.getAttribute('data-i18n').split(';'); // Если несколько ключей, разделенных ;
      let text = '';
      keys.forEach(key => {
        text += i18next.t(key.trim()) + ' '; // Получаем перевод для каждого ключа и добавляем к тексту
      });
      el.innerHTML = text.trim(); // Устанавливаем HTML-содержимое элемента
    });
  }
  
document.addEventListener('DOMContentLoaded', function() {
// Select all elements with the 'language-link' class
var languageLinks = document.querySelectorAll('.lang-link');

languageLinks.forEach(function(link) {
   // Attach a click event listener to each link
   link.addEventListener('click', function(e) {
       // Prevent the default link action
       e.preventDefault();

       // Change the language to the one specified in the 'data-lang' attribute
       var lang = this.getAttribute('data-lang');
       i18next.changeLanguage(lang, () => {
        updateTexts(); // Обновляем тексты после смены языка
       });


       // Remove the 'active' class from all links
       languageLinks.forEach(function(lnk) {
           lnk.classList.remove('active');
       });

       // Add the 'active' class to the clicked link
       this.classList.add('active');
   });
});
});


 function sliders() {
    let viewport;
    var p = new Splide('.problem-slider').mount();;
    p.sync(p);
    var s = new Splide('.quote-slider').mount();
    s.sync(s);
    var b = new Splide('.fb-slider').mount();
    b.sync(b);
    var t = new Splide('.team-slider');

    t.on("ready", () => {
       viewport = window.innerWidth;
       t.go('+1');
    })

    onresize = () => {
       if ((viewport >= 768) && (window.innerWidth < 768)) {
          t.destroy({ "completely": true });
          t.mount();
          t.sync(t);

       }
       if ((viewport < 768) && (window.innerWidth >= 768)) {
          t.destroy({ "completely": true });
          t.mount();
          t.sync(t);

       }
    }
    t.mount();
    t.sync(t);
 }
